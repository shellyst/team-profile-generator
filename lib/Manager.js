const Employee = require("./Employee");

//Manager class extends Employee class to access the parent data, as well as Office Number and Role which are specific to the Manager class.
class Manager extends Employee {
  constructor(name, id, email, officeNumber) {
    super(name, id, email);
    this.officeNumber = officeNumber;
  }

  getOfficeNumber() {
    return this.officeNumber;
  }

  getRole() {
    return "Manager";
  }
}

module.exports = Manager;
